    public void buscarProdutos(int pagina) {

        int offset = (pagina * 3) - 3;

        ProdutoController controller = new ProdutoController();

        List<Produto> lista = controller.consultar(offset);

        Produto produto;

        int tamanho = lista.size();
        if (tamanho >= 1) {
            pnlProd1.setVisible(true);

            produto = lista.get(0);

            if (produto != null) {
                lbNome1.setText(produto.getNome());
                produto.getEstoque();
                lbPreco1.setText("R$" + String.valueOf(produto.getPreco()));
                lbProduto1.setIcon(produto.getImagem());
            }
        } else {
            pnlProd1.setVisible(false);
        }

        if (tamanho >= 2) {
            pnlProd2.setVisible(true);
            produto = lista.get(1);

            if (produto != null) {
                lbNome2.setText(produto.getNome());
                produto.getEstoque();
                lbPreco2.setText("R$" + String.valueOf(produto.getPreco()));
                lbProduto2.setIcon(produto.getImagem());
            }

        } else {
            pnlProd2.setVisible(false);
        }

        if (tamanho >= 3) {
            pnlProd3.setVisible(true);
            produto = lista.get(2);

            if (produto != null) {
                lbNome3.setText(produto.getNome());
                produto.getEstoque();
                lbPreco3.setText("R$" + String.valueOf(produto.getPreco()));
                lbProduto3.setIcon(produto.getImagem());
            }
        } else {
            pnlProd3.setVisible(false);
        }
    }

